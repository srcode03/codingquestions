class Solution {
  public:
    int canReach(int A[], int N) {
        // code here
        int i=0;
        int maxreach=0;
        while(i<N && i<=maxreach)
        {
            maxreach=max(maxreach,A[i]+i);
            i++;
        }
        if(i==N)
        {
            return true;
        }
        return false;
    }
};

